    private void btnCreateCustActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateCustActionPerformed
        // TODO add your handling code here:
        if (ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(txtdelusername.getText())) {
            UserAccount userAccount = ecosystem.getUserAccountDirectory().createUserAccount(txtdelname.getText(), txtdelusername.getText(), txtdelpassword.getText(), null, new DeliverManRole());
            DeliveryMan deliveryman = ecosystem.getDeliveryManDirectory().createUserAccount(txtdelusername.getText());
            populateDeliveryTable();
            txtdelname.setText("");
            txtdelusername.setText("");
            txtdelpassword.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "Username is not unique");
        }

    }//GEN-LAST:event_btnCreateCustActionPerformed

