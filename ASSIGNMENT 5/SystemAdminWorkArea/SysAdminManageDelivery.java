    public void populateDeliveryTable() {

        DefaultTableModel tablemodel = (DefaultTableModel) tblDelivery.getModel();

        tablemodel.setRowCount(0);
        for (UserAccount user : ecosystem.getUserAccountDirectory().getUserAccountList()) {
            if (user.getRole().getClass().getName() == "Business.Role.DeliverManRole") {
                Object[] row = new Object[3];
                row[0] = user.getName();
                row[1] = user.getUsername();
                row[2] = user.getPassword();

                tablemodel.addRow(row);
            }
        }
    }
    
    
    
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectRow = tblDelivery.getSelectedRow();

        if(selectRow>=0){
            String name = (String) tblDelivery.getValueAt(selectRow, 0);
            String username= (String) tblDelivery.getValueAt(selectRow, 1);
            String password= (String) tblDelivery.getValueAt(selectRow, 2);
            user=ecosystem.getUserAccountDirectory().authenticateUser(username, password);

            txtdelname.setText(name+"");
            txtdelusername.setText(username+"");
            txtdelpassword.setText(password+"");

            JOptionPane.showMessageDialog(null,"Please press save button to save the profile after updating the text field");

        }
        else {
            JOptionPane.showMessageDialog(null,"Please select a row to update a profile");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

